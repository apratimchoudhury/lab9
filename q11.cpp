#include<iostream>
using namespace std;

int main()
{
    int a,*p;
    char b,*q;
    double c,*r;
    bool d,*s;
    float e,*t;
    cout<<"int variable size: "<<sizeof(a)<<"int pointer size: "<<sizeof(p)<<endl;
    cout<<"char variable size: "<<sizeof(b)<<"int pointer size: "<<sizeof(q)<<endl;
    cout<<"double variable size: "<<sizeof(c)<<"int pointer size: "<<sizeof(r)<<endl;
    cout<<"bool variable size: "<<sizeof(d)<<"int pointer size: "<<sizeof(s)<<endl;
    cout<<"float variable size: "<<sizeof(e)<<"int pointer size: "<<sizeof(t)<<endl;
    return 0;
}
